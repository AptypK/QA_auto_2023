# Реалізувати програму, яка обчислює і виводить на екран суму всіх чисел
# від 1 до введеного користувачем числа включно.
# Dev: Artur Kychenko
# Date: 27.04.2023

number = int(input("Введіть число"))
i = 1
sum = 0

while i <= number:
    sum += i
    i += 1

print(sum)
