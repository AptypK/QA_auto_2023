n = int(input(print("Введіть значення n:")))
factorial = 1
i = 1

# for i in range(n, 1, -1): #Альтернативний варіант, але рухаємося від n до 1
# якщо від 1 до n, то остання операція не виконується так як початок від 0
while i <= 5:
    factorial *= i
    i += 1
print("Факторіал числа ", n, "складає:", factorial)
