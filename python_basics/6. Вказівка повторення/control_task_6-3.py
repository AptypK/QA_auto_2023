# Реалізувати програму, яка виводить послідовність чисел від 0 до введеного користувачем числа включно.
# Кожне парне число має бути піднесено в квадрат перед виведенням, непарні числа виводяться без змін.
# Dev: Artur Kychenko
# Date: 27.04.2023

number = int(input("Введіть число"))
i = 0
while i <= number:
    if i % 2 == 0:
        print(i * i)
    else:
        print(i)
    i += 1
